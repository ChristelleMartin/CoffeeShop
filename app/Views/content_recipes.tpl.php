    <div class="recipe">
        <h2>4. Tiramisu</h2>
        <h3>Ingrédients :</h3>
        <ul>
            <li>3 œufs</li>
            <li>100 g de sucre</li>
            <li>250 g de mascarpone</li>
            <li>200 ml de café fort</li>
            <li>1 paquet de biscuits à la cuillère</li>
            <li>Cacao en poudre pour la décoration</li>
        </ul>
        <h3>Instructions :</h3>
        <p>Séparez les blancs des jaunes d'œufs. Battez les jaunes avec le sucre jusqu'à ce que le mélange blanchisse, puis ajoutez le mascarpone. Montez les blancs en neige et incorporez-les délicatement au mélange. Trempez les biscuits dans le café et disposez-les dans un plat. Recouvrez de crème, répétez l'opération et terminez par une couche de crème. Saupoudrez de cacao et réfrigérez pendant au moins 4 heures.</p>
    </div>

    <div class="recipe">
        <h2>5. Mocha Frappé</h2>
        <h3>Ingrédients :</h3>
        <ul>
            <li>1 tasse de café refroidi</li>
            <li>1 tasse de lait</li>
            <li>2 cuillères à soupe de chocolat en poudre</li>
            <li>1 cuillère à soupe de sucre</li>
            <li>Glaçons</li>
            <li>Crème fouettée pour la garniture</li>
        </ul>
        <h3>Instructions :</h3>
        <p>Mélangez le café, le lait, le chocolat en poudre, le sucre et les glaçons dans un mixeur. Mixez jusqu'à obtenir une consistance lisse. Versez dans un verre et garnissez de crème fouettée.</p>
    </div>

    <div class="recipe">
        <h2>6. Granité de Café</h2>
        <h3>Ingrédients :</h3>
        <ul>
            <li>500 ml de café fort</li>
            <li>100 g de sucre</li>
            <li>1 cuillère à café d'extrait de vanille</li>
            <li>Crème fouettée pour la garniture</li>
        </ul>
        <h3>Instructions :</h3>
        <p>Mélangez le café, le sucre et l'extrait de vanille jusqu'à ce que le sucre soit dissous. Versez dans un plat peu profond et placez au congélateur. Grattez avec une fourchette toutes les 30 minutes pour former des cristaux. Répétez jusqu'à ce que la texture soit granuleuse. Servez dans des verres et garnissez de crème fouettée.</p>
    </div>

