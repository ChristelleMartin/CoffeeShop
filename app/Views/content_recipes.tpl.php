    <div class="recipe">
        <h2>8. Gâteau au Café</h2>
        <h3>Ingrédients :</h3>
        <ul>
            <li>200 g de beurre</li>
            <li>200 g de sucre</li>
            <li>4 œufs</li>
            <li>200 g de farine</li>
            <li>2 cuillères à soupe de café fort</li>
            <li>1 cuillère à café de levure chimique</li>
            <li>Glace au café pour la garniture</li>
        </ul>
        <h3>Instructions :</h3>
        <p>Préchauffez le four à 180°C. Battez le beurre et le sucre jusqu'à obtenir une consistance crémeuse. Ajoutez les œufs un par un. Incorporez la farine et la levure, puis le café. Versez la pâte dans un moule et enfournez pendant 30 minutes. Laissez refroidir avant de décorer avec la glace au café.</p>
    </div>

    <div class="recipe">
        <h2>9. Crème Brûlée au Café</h2>
        <h3>Ingrédients :</h3>
        <ul>
            <li>500 ml de crème liquide</li>
            <li>5 jaunes d'œufs</li>
            <li>100 g de sucre</li>
            <li>1 cuillère à soupe de café instantané</li>
            <li>Sucre pour la caramélisation</li>
        </ul>
        <h3>Instructions :</h3>
        <p>Préchauffez le four à 150°C. Chauffez la crème avec le café jusqu'à ce qu'elle frémisse. Fouettez les jaunes d'œufs avec le sucre jusqu'à ce que le mélange blanchisse. Ajoutez la crème chaude en fouettant. Versez dans des ramequins et faites cuire au bain-marie pendant 30 minutes. Laissez refroidir, saupoudrez de sucre et caramélisez avec un chalumeau.</p>
    </div>
